from __future__ import print_function
import os, re, sys

################## FONT COLORS ##################
# Begin Red color
def begin_red_fontcolor():
    print("\033[1;31m")

# End Red color
def end_red_fontcolor():
    print("\033[1;m")

# Begin Green color
def begin_green_fontcolor():
    print("\033[32;1m")

# End green color
def end_green_fontcolor():
    print("\033[0m")


